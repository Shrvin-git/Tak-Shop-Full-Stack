"use client";

import Pagination from "@/components/shared/Pagination";
import Box from "@/components/templates/admin-panel/comments/Box";
import Table from "@/components/templates/admin-panel/comments/Table";
import styles from "@/styles/p-admin/comments.module.css";
import FilterControls from "@/utils/FilterControls";
import { useEffect, useMemo, useState } from "react";

function page() {
  const [comments, setComments] = useState([]);
  const [loading, setLoading] = useState(true);
  const [filter, setFilter] = useState("all");
  const [currentPage, setCurrentPage] = useState(1); // ۱. اضافه کردن وضعیت صفحه
  const pageSize = 10; // تعداد آیتم در هر صفحه

  useEffect(() => {
    const fetchComments = async () => {
      try {
        const res = await fetch("/api/comment");
        if (!res.ok) throw new Error("خطا در دریافت نظرات");
        const data = await res.json();
        setComments(data.comments || []);
      } catch (err) {
        console.error(err);
      } finally {
        setLoading(false);
      }
    };
    fetchComments();
  }, []);

  const filterOptions = [
    { value: "all", title: "همه نظرات" },
    { value: "accepted", title: "تایید شده" },
    { value: "pending", title: "در انتظار تایید" },
    { value: "rejected", title: "رد شده" },
  ];

  const filterLogic = {
    all: (comments) => comments,
    accepted: (comments) => comments.filter((c) => c.isAccept === true),
    pending: (comments) => comments.filter((c) => c.isAccept === null),
    rejected: (comments) => comments.filter((c) => c.isAccept === false),
  };

  const filteredComments = useMemo(() => {
    const filterFn = filterLogic[filter];
    return filterFn ? filterFn(comments) : comments;
  }, [filter, comments]);

  const handleFilterChange = (newFilter) => {
    setFilter(newFilter);
    setCurrentPage(1);
  };

  const paginatedComments = useMemo(() => {
    const startIndex = (currentPage - 1) * pageSize;
    return filteredComments.slice(startIndex, startIndex + pageSize);
  }, [filteredComments, currentPage]);

  return (
    <div className={styles["admin-page"]}>
      <div className={styles["admin-page__content"]}>
        <div className={styles["main-info"]}>
          {/* <Box />
          <Box />
          <Box />
          <Box /> */}
        </div>
        <div className={styles["comments-wrapper"]}>
          <span className={styles["product-table-header"]}>لیست نظرات </span>

          <FilterControls
            options={filterOptions}
            value={filter}
            onChange={handleFilterChange} // استفاده از تابع جدید
          />

          <Table comments={paginatedComments} />

          <Pagination
            totalItems={filteredComments}
            pageSize={pageSize}
            currentPage={currentPage}
            onPageChange={setCurrentPage}
          />
        </div>
      </div>
    </div>
  );
}

export default page;

